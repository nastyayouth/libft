#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*s1;
	size_t			i;

	s1 = s;
	if (n == 0)
		return ;
	i = 0;
	while (i < n)
	{
		s1[i] = '\0';
		i++;
	}
}

char *ft_strrchr(const char *s, int c)
{
	int		i;

	while (s[i] != (char)c)
	{ 
	if (*s == '\0')
			return (0);
		*s++;
		i++;
	}
	while (*s == (char)c && s[i] != (char)c)
		i++;
		*s++;
	return (&s);
}
